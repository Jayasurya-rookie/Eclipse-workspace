/*
 * RELATIONAL OPERATORS
 */
package logicOperator;

public class relational_Operator {
	public static void main(String []args) {
		int a=10,b=10;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
	}

}
