package com.kodnest.cc;

public class encapsualtion {
	String name;
	String act;
	public void encapsualtion(String name, String act) {
		this.name = name;
		this.act=act;
	}
	public void setName(String name){
		this.name=name;
	}
	public void setAct(String act) {
		this.act=act;
	}
	public String getName() {
		return name;
	}
	public String getAct() {
		return act;
	}
	

}
